import pandas as pd  # استيراد مكتبة pandas للعمل على البيانات

# قراءة البيانات من ملف Excel
data = pd.read_excel('test1.xlsx')  # قراءة بيانات من ملف Excel باسم test1.xlsx

# طباعة أول 5 صفوف من البيانات لرؤية الشكل العام للبيانات
print(data.head())  # طباعة أول 5 صفوف من البيانات

# الحصول على معلومات عن البيانات مثل عدد الصفوف والأعمدة
print(data.info())  # طباعة معلومات البيانات مثل الأعمدة وأنواع البيانات

# حساب إحصائيات ملخصة للبيانات الرقمية
print(data.describe())  # حساب الإحصائيات الوصفية للبيانات الرقمية

# معالجة البيانات الناقصة (إن وجدت)
data = data.fillna(0)  # ملء القيم الفارغة بالصفر

# تأكد من أن العمود الذي تريد تصفيته موجود في البيانات
if 'column_name' in data.columns:
    # تصفية البيانات بناءً على شرط معين (مثلاً: قيمة أكبر من 100)
    filtered_data = data[data['column_name'] > 100]  # تصفية البيانات بناءً على شرط على عمود معين

    # طباعة البيانات المصفاة
    print(filtered_data)  # طباعة البيانات المصفاة

    # إذا أردنا حفظ البيانات المعدلة في ملف جديد
    filtered_data.to_excel('filtered_test1.xlsx', index=False)  # حفظ البيانات المصفاة في ملف Excel جديد
else:
    print("العمود الذي تم ذكره غير موجود في البيانات.")
